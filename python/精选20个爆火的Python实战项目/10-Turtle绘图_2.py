# Turtle绘图
import turtle
wn = turtle.Screen()
turtle.bgcolor('black')
turtle.color('yellow')

turtle.forward(100)
turtle.backward(50)
turtle.left(45)
turtle.color('red')
turtle.forward(100)
turtle.right(90)
turtle.color('white')
turtle.forward(100)
turtle.right(135)
turtle.color('purple')
turtle.forward(100)
wn.exitonclick()

