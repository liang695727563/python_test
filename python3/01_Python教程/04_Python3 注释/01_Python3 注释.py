'''
Python 中的注释有单行注释和多行注释：
Python 中单行注释以 # 开头
'''

# 这是一个注释
print('Hello world')


# 多行注释用三个单引号（'''）或者三个双引号（"""）将需要注释的内容囊括起来
# 1、单引号（'''）
'''
这是多行注释，用三个单引号
这是多行注释，用三个单引号 
这是多行注释，用三个单引号
'''
print("Hello, World!") 

# 2、双引号（"""）

"""
这是多行注释，用三个双引号
这是多行注释，用三个双引号 
这是多行注释，用三个双引号
"""
print("Hello, World!") 

#  三引号实际上是多行长字符串的声明方式，也作为python代码文档使用（类似javadoc）。


